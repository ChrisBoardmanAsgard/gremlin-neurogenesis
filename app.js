  function cleanupRuntime() {
    state.evolving = false;
    cancelAnimationFrame(state.topologyRaf);
    state.topologyRaf = 0;
    if (state.statusTimer) clearInterval(state.statusTimer);
    if (state.ws) {
      state.ws.close();
      state.ws = null;
    }
    if (state.backgroundWorker) {
      state.backgroundWorker.terminate();
      state.backgroundWorker = null;
    }
  }

  function boot() {
    el("corpusText").value = DEFAULT_SEED_TEXT;
    state.lab.setCorpus(DEFAULT_SEED_TEXT);
    bindEvents();
    try {
      if (location.protocol === "file:") {
        log("Opened as a local file, so browser Web Workers are blocked. Using main-thread evolution. Launch the local server for background workers.");
      } else {
        state.backgroundWorker = new Worker("local-evolve-worker.js");
        log("Background Web Worker ready for browser-side evolution cycles.");
      }
    } catch (error) {
      log(`Background Web Worker unavailable. Using main-thread evolution. ${error.message}`);
    }
    log("Ready. This app uses no external AI model or AI API.");
    log(`400 neurons is the default seed size; the current UI allows up to ${MAX_NEURONS.toLocaleString()} neurons and ${MAX_SYNAPSES.toLocaleString()} synapses.`);
    readyToTrain();
    updateReadout();
    renderImage();
    if (location.hash === "#topology") toggleTopologyView();
    connectToolSocket();
    refreshNetworkStatus();
    state.statusTimer = setInterval(refreshNetworkStatus, 5000);
    window.addEventListener("beforeunload", cleanupRuntime, { once: true });
  }

  boot();
