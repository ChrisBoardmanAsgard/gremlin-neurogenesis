  function readFileAsText(file, options = {}) {
    return new Promise((resolve, reject) => {
      const reader = new FileReader();
      reader.onload = () => {
        const text = String(reader.result || "");
        const maxChars = options.maxChars === Infinity ? Infinity : Number(options.maxChars ?? MAX_IMPORTED_TEXT_CHARS);
        resolve(Number.isFinite(maxChars) && text.length > maxChars ? text.slice(0, maxChars) : text);
      };
      reader.onerror = () => reject(reader.error);
      reader.readAsText(file);
    });
  }

  async function readPdfAsText(file) {
    if (location.protocol === "file:") {
      throw new Error("PDF import needs the local server. Open http://localhost:4173 with launch-genesis-lab.bat, then drop the PDF again.");
    }
    if (file.size > 25 * 1024 * 1024) {
      throw new Error("PDF is too large for safe import. Try a smaller PDF or split it first.");
    }
    const response = await fetch(`/api/import/pdf?name=${encodeURIComponent(file.name)}`, {
      method: "POST",
      headers: { "Content-Type": "application/pdf" },
      body: await file.arrayBuffer()
    });
    const payload = await response.json();
    if (!response.ok || !payload.ok) throw new Error(payload.error || "PDF import failed");
    return payload;
  }

  function imageToTarget(image, name = "image target") {
    const size = 48;
    const canvas = document.createElement("canvas");
    canvas.width = size;
    canvas.height = size;
    const ctx = canvas.getContext("2d");
    ctx.drawImage(image, 0, 0, size, size);
    const imageData = ctx.getImageData(0, 0, size, size);
    return { name, size, pixels: imageData.data, thumbnail: canvas };
  }

  function loadImageTarget(file) {
    return new Promise((resolve, reject) => {
      const reader = new FileReader();
      const image = new Image();
      reader.onload = () => {
        image.onload = () => {
          try {
            resolve(imageToTarget(image, file.name));
          } catch (error) {
            reject(error);
          }
        };
        image.onerror = reject;
        image.src = String(reader.result);
      };
      reader.onerror = () => reject(reader.error);
      reader.readAsDataURL(file);
    });
  }

  function parseYouTubeId(value) {
    const text = String(value || "").trim();
    if (!text) return "";
    const match = text.match(/(?:youtube\.com\/(?:watch\?v=|shorts\/|embed\/)|youtu\.be\/)([A-Za-z0-9_-]{6,})/i);
    return match ? match[1] : "";
  }

