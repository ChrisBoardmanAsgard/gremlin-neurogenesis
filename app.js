  async function evolveStep(imageOptions = {}) {
    syncConfigToLab();
    const imageTargets = state.imageTargets.length ? state.imageTargets : [];
    if (state.backgroundWorker && !imageOptions.forceMainThread) {
      return evolveStepInWorker(imageOptions);
    }
    const result = state.lab.evolveOnce({
      imageTarget: imageOptions.imageTarget || null,
      imageTargets,
      imagePrompt: imageOptions.imagePrompt || el("imagePrompt").value,
      imageLearningRate: Number(el("imageMutation")?.value || 0.012) * 0.5,
      maxChars: Number(state.lab.config.neurons) > 1800 ? 420 : 760
    });
    if (result.imageTarget) state.lastImageTarget = result.imageTarget;
    state.lastCycleAt = Date.now();
    const maxNeurons = Math.max(...state.lab.population.map(genome => genome.neurons));
    const minNeurons = Math.min(...state.lab.population.map(genome => genome.neurons));
    const maxSynapses = Math.max(...state.lab.population.map(genome => genome.synapses));
    const minSynapses = Math.min(...state.lab.population.map(genome => genome.synapses));
    const imageNote = result.imageTarget ? `, image "${result.imageTarget.name}" loss ${Number(result.imageLoss).toFixed(4)}` : "";
    log(`Generation ${state.lab.generation}: fitness ${result.best.fitness.toFixed(4)}, loss ${result.best.loss.toFixed(3)}, best ${result.best.neurons}n/${result.best.synapses}s, population ${minNeurons}-${maxNeurons}n and ${minSynapses}-${maxSynapses}s${imageNote}.`);
    updateReadout();
    return result;
  }

  function evolveStepInWorker(imageOptions = {}) {
    if (state.workerBusy) return Promise.resolve({ best: state.lab.best(), elapsed: 0, queued: true });
    state.workerBusy = true;
    const started = performance.now();
    const id = `local-${Date.now().toString(36)}`;
    const imageTargets = state.imageTargets.map(serializableImageTarget).filter(Boolean);
    const imageTarget = serializableImageTarget(imageOptions.imageTarget || null);
    return new Promise(resolve => {
      const timeout = setTimeout(() => {
        state.workerBusy = false;
        log("Background worker took too long; pausing this cycle to keep the browser responsive.");
        resolve({ best: state.lab.best(), elapsed: 0, timeout: true });
      }, 8000);
      state.backgroundWorker.onmessage = event => {
        const payload = event.data || {};
        if (payload.id !== id) return;
        clearTimeout(timeout);
        state.workerBusy = false;
        if (!payload.ok) {
          log(`Background worker failed: ${payload.error}`);
          resolve({ best: state.lab.best(), elapsed: 0 });
          return;
        }
        state.lab.corpus = payload.corpus;
        state.lab.corpora = payload.corpora;
        state.lab.persistentContext = payload.persistentContext || "";
        state.lab.curriculumLevel = payload.curriculumLevel || state.lab.curriculumLevel;
        state.lab.setConfig(payload.config || {});
        const champion = state.lab.importChampion(payload.champion);
        state.lab.generation = payload.generation;
        if (payload.historyPoint) state.lab.history.push(payload.historyPoint);
        if (state.lab.history.length > 160) state.lab.history.shift();
        const elapsed = Math.round(performance.now() - started);
        const maxNeurons = Math.max(...state.lab.population.map(genome => genome.neurons));
        const minNeurons = Math.min(...state.lab.population.map(genome => genome.neurons));
        log(`Generation ${state.lab.generation}: fitness ${champion.fitness.toFixed(4)}, loss ${champion.loss.toFixed(3)}, best ${champion.neurons}n/${champion.synapses}s, population ${minNeurons}-${maxNeurons}n. Background worker.`);
        updateReadout();
        resolve({ best: champion, elapsed });
      };
      state.backgroundWorker.postMessage({
        type: "evolve",
        id,
        corpus: state.lab.corpus,
        corpora: state.lab.corpora,
        persistentContext: state.lab.persistentContext,
        curriculumLevel: state.lab.curriculumLevel,
        config: state.lab.config,
        generation: state.lab.generation,
        champion: state.lab.best().toJSON(),
        imageTargets,
        imageTarget,
        imagePrompt: imageOptions.imagePrompt || el("imagePrompt").value,
        imageLearningRate: Number(el("imageMutation")?.value || 0.012) * 0.5,
        maxChars: Number(state.lab.config.neurons) > 1800 ? 420 : 760
      });
    });
  }

  async function evolveLoop() {
    if (!state.evolving) return;
    setStatus("Evolving");
    await new Promise(resolve => setTimeout(resolve, 20));
    try {
      await evolveStep();
    } catch (error) {
      log(`Evolution stopped: ${error.message}`);
      state.evolving = false;
      el("evolveButton").textContent = "Evolve";
      setStatus("Error");
      return;
    }
    requestAnimationFrame(evolveLoop);
  }

  async function importWikipedia() {
    const title = el("wikiTitle").value.trim();
    if (!title) return;
    setStatus("Fetching Wikipedia");
    try {
      const url = `https://en.wikipedia.org/w/api.php?origin=*&action=query&prop=extracts&explaintext=1&redirects=1&format=json&titles=${encodeURIComponent(title)}`;
      const response = await fetch(url);
      const data = await response.json();
      const pages = data?.query?.pages || {};
      const page = Object.values(pages)[0];
      if (!page?.extract) throw new Error("No article text returned");
      const addition = `\n\n# ${page.title}\n${page.extract}`;
      el("corpusText").value = `${el("corpusText").value}${addition}`.trim();
      state.lab.setCorpus(el("corpusText").value);
      log(`Imported Wikipedia article "${page.title}" (${formatChars(page.extract.length)} chars).`);
      setStatus("Imported");
      updateReadout();
    } catch (error) {
      log(`Wikipedia import failed: ${error.message}`);
      setStatus("Import failed");
    }
  }

  function cleanWikitext(text) {
    return String(text || "")
      .replace(/\{\{[^{}]*\}\}/g, " ")
      .replace(/<ref[^>]*>[\s\S]*?<\/ref>/gi, " ")
      .replace(/<[^>]+>/g, " ")
      .replace(/\[\[File:[^\]]+\]\]/gi, " ")
      .replace(/\[\[Category:[^\]]+\]\]/gi, " ")
      .replace(/\[\[([^\]|]+)\|([^\]]+)\]\]/g, "$2")
      .replace(/\[\[([^\]]+)\]\]/g, "$1")
      .replace(/={2,}/g, "\n")
      .replace(/\s+\n/g, "\n")
      .replace(/\n{3,}/g, "\n\n")
      .trim();
  }

  async function fetchFullWikipediaArticle(title) {
    const url = `https://en.wikipedia.org/w/api.php?origin=*&action=query&prop=revisions&rvprop=content&rvslots=main&format=json&formatversion=2&redirects=1&titles=${encodeURIComponent(title)}`;
    const response = await fetch(url);
    const data = await response.json();
    const page = data?.query?.pages?.[0];
    const content = page?.revisions?.[0]?.slots?.main?.content;
    if (!content) throw new Error(`No full article returned for ${title}`);
    return { title: page.title || title, text: cleanWikitext(content) };
  }

  async function importWikipediaBatch() {
    const titles = el("wikiBatch").value.split(/\r?\n/).map(title => title.trim()).filter(Boolean);
    if (!titles.length) return;
    setStatus("Loading full articles");
    for (let i = 0; i < titles.length; i++) {
      try {
        const article = await fetchFullWikipediaArticle(titles[i]);
        state.lab.addCorpus(article.title, article.text, Math.min(10, i + 1));
        log(`Loaded full Wikipedia article "${article.title}" as curriculum difficulty ${Math.min(10, i + 1)} (${formatChars(article.text.length)} chars).`);
      } catch (error) {
        log(`Full article load failed for "${titles[i]}": ${error.message}`);
      }
    }
    el("corpusText").value = state.lab.corpus;
    updateReadout();
    setStatus("Curriculum loaded");
  }

  function readFileAsText(file) {
    return new Promise((resolve, reject) => {
      const reader = new FileReader();
      reader.onload = () => {
        const text = String(reader.result || "");
        resolve(text.length > MAX_IMPORTED_TEXT_CHARS ? text.slice(0, MAX_IMPORTED_TEXT_CHARS) : text);
      };
      reader.onerror = () => reject(reader.error);
      reader.readAsText(file);
    });
  }

  async function readPdfAsText(file) {
    if (location.protocol === "file:") {
      throw new Error("PDF import needs the local server. Open http://localhost:4173 with launch-genesis-lab.bat, then drop the PDF again.");
    }
    if (file.size > 25 * 1024 * 1024) {
      throw new Error("PDF is too large for safe import. Try a smaller PDF or split it first.");
    }
    const response = await fetch(`/api/import/pdf?name=${encodeURIComponent(file.name)}`, {
      method: "POST",
      headers: { "Content-Type": "application/pdf" },
      body: await file.arrayBuffer()
    });
    const payload = await response.json();
    if (!response.ok || !payload.ok) throw new Error(payload.error || "PDF import failed");
    return payload;
  }

  function loadImageTarget(file) {
    return new Promise((resolve, reject) => {
      const reader = new FileReader();
      const image = new Image();
      reader.onload = () => {
        image.onload = () => {
          const size = 48;
          const canvas = document.createElement("canvas");
          canvas.width = size;
          canvas.height = size;
          const ctx = canvas.getContext("2d");
          ctx.drawImage(image, 0, 0, size, size);
          const imageData = ctx.getImageData(0, 0, size, size);
          resolve({ name: file.name, size, pixels: imageData.data, thumbnail: canvas });
        };
        image.onerror = reject;
        image.src = String(reader.result);
      };
      reader.onerror = () => reject(reader.error);
      reader.readAsDataURL(file);
    });
  }

  async function handleFiles(files) {
    setStatus("Importing files");
    for (const file of files) {
      try {
        if (file.type.startsWith("image/")) {
          saveBrowserCheckpoint("before-image-import");
          const target = await loadImageTarget(file);
          state.imageTargets.push(target);
          state.lastImageTarget = target;
          state.lab.best().seeImage(target, 0.22);
          state.lab.lastImageLoss = state.lab.best().trainImage(target, file.name, 0.018);
          log(`Imported image target "${file.name}" and activated visual training (initial loss ${state.lab.lastImageLoss.toFixed(4)}).`);
        } else if (file.type === "application/pdf" || file.name.toLowerCase().endsWith(".pdf")) {
          log(`Extracting text from PDF "${file.name}" safely on the local server...`);
          const pdf = await readPdfAsText(file);
          state.lab.addCorpus(pdf.name || file.name, pdf.text, Number(el("corpusDifficulty")?.value) || 1);
          el("corpusText").value = state.lab.corpus;
          log(`Imported PDF "${file.name}" (${formatChars(pdf.chars || pdf.text.length)} chars${pdf.truncated ? ", capped for safety" : ""}).`);
        } else {
          const text = await readFileAsText(file);
          state.lab.addCorpus(file.name, text, Number(el("corpusDifficulty")?.value) || 1);
          el("corpusText").value = state.lab.corpus;
          log(`Imported text file "${file.name}" (${formatChars(text.length)} chars${file.size > text.length ? ", capped for safety" : ""}).`);
        }
      } catch (error) {
        log(`Could not import "${file.name}": ${error.message}`);
      }
      await new Promise(resolve => setTimeout(resolve, 0));
    }
    state.lab.rebuildCurriculumCorpus();
    el("corpusText").value = state.lab.corpus;
    renderTargets();
    updateReadout();
    setStatus("Import complete");
  }

  function renderTargets() {
    const strip = el("targetStrip");
    strip.innerHTML = "";
    for (const target of state.imageTargets.slice(-5).reverse()) {
      const card = document.createElement("div");
      card.className = "target-card";
      const canvas = document.createElement("canvas");
      canvas.width = target.size;
      canvas.height = target.size;
      canvas.getContext("2d").putImageData(new ImageData(new Uint8ClampedArray(target.pixels), target.size, target.size), 0, 0);
      const label = document.createElement("span");
      label.textContent = target.name;
      card.append(canvas, label);
      strip.append(card);
    }
  }

  function restoreImageTargets(targets) {
    if (!Array.isArray(targets)) return;
    state.imageTargets = targets
      .map(target => {
        const size = Number(target.size) || 48;
        const rawPixels = target.pixels?.value || target.pixels || [];
        const pixels = Array.isArray(rawPixels)
          ? rawPixels
          : ArrayBuffer.isView(rawPixels)
            ? Array.from(rawPixels)
            : Object.keys(rawPixels).sort((a, b) => Number(a) - Number(b)).map(key => rawPixels[key]);
        if (pixels.length < size * size * 4) return null;
        return {
          name: target.name || "image target",
          size,
          pixels: new Uint8ClampedArray(pixels.slice(0, size * size * 4).map(value => clamp(Number(value) || 0, 0, 255)))
        };
      })
      .filter(Boolean);
    state.lastImageTarget = state.imageTargets.at(-1) || null;
    renderTargets();
  }

