  function connectToolSocket() {
    if (location.protocol === "file:") {
      setToolStatus("Local file mode. Start the Node server for controlled internet tool use.");
      return;
    }
    try {
      if (state.ws) state.ws.close();
      const protocol = location.protocol === "https:" ? "wss:" : "ws:";
      state.ws = new WebSocket(`${protocol}//${location.host}/ws`);
      state.ws.addEventListener("message", event => {
        const payload = JSON.parse(event.data);
        if (payload.type === "thinking") {
          if (payload.phase === "searching") {
            const labels = (payload.commands || []).map(command => `${command.kind}:${command.value}`).join(", ");
            setToolStatus(`Searching: ${labels}`);
          } else if (payload.phase === "generating") {
            setToolStatus("Thinking...");
          } else if (payload.phase === "done") {
            setToolStatus(payload.toolSteps ? `Tool sense complete: ${payload.toolSteps} result(s).` : "Thinking complete.");
          }
        }
      if (payload.type === "tool") {
          setToolStatus(payload.ok ? `Tool result: ${payload.command} ${payload.value}` : `Tool blocked: ${payload.error}`);
        }
        if (payload.type === "evolution" && payload.immigrants) {
          log(`Server diversity refresh: ${payload.immigrants} fresh 400-neuron immigrant genomes added.`);
        }
        if (payload.type === "evolution" && payload.selfGenerated) {
          log(`Server self-training accepted ${payload.selfGenerated} generated dialogue pair(s).`);
        }
        if (payload.type === "evolution" && payload.spiral?.active) {
          log(`🌀 Server Spiral Mode Active — ${payload.spiral.remaining} generation(s) left, mirror +${payload.mirrorAccepted || 0}.`);
        }
      });
      state.ws.addEventListener("open", () => setToolStatus("Controlled internet sense connected."));
      state.ws.addEventListener("close", () => setToolStatus("Tool socket closed. Local/server HTTP still works."));
    } catch (error) {
      setToolStatus(`Tool socket unavailable: ${error.message}`);
    }
  }

  function updateServerEvolutionStatus(snapshot) {
    if (!el("serverStateMetric")) return;
    if (!snapshot) {
      el("serverStateMetric").textContent = "offline";
      el("serverGenMetric").textContent = "--";
      el("serverSaveMetric").textContent = "--";
      return;
    }
    el("serverStateMetric").textContent = snapshot.running ? "running" : "paused";
    el("serverGenMetric").textContent = `Gen ${snapshot.generation || 0}`;
    el("serverSaveMetric").textContent = snapshot.savePath ? "disk" : "--";
    if (snapshot.spiral?.active) setToolStatus(`Server Spiral Mode Active: ${snapshot.spiral.remaining} generation(s) left.`);
  }

  function serverPayload() {
    syncConfigToLab();
    return {
      corpus: state.lab.corpus,
      corpora: sanitizeCorpora(state.lab.corpora).corpora,
      persistentContext: sanitizePersistentContext(state.lab.persistentContext, 16000),
      userProfile: sanitizePersistentContext(state.lab.userProfile || "", 3000),
      memorySummary: sanitizePersistentContext(state.lab.memorySummary || "", 6000),
      recentTranscript: (state.lab.recentTranscript || []).slice(-32),
      memoryBank: sanitizeMemoryBank(state.lab.memoryBank, 240),
      mirrorCorpus: (state.lab.mirrorCorpus || []).slice(-80),
      spiralPhase: state.lab.spiralPhase || null,
      curriculumLevel: state.lab.curriculumLevel,
      imageTargets: state.imageTargets.map(target => ({
        name: target.name,
        size: target.size,
        pixels: Array.from(target.pixels)
      })),
      config: state.lab.config,
      champion: state.lab.best().toJSON(),
      history: state.lab.history.slice(-160),
      delayMs: 180
    };
  }

  async function startServerEvolution() {
    const response = await fetch("/api/server/start", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ delayMs: 180 })
    });
    const payload = await response.json();
    updateServerEvolutionStatus(payload.serverEvolution);
    log("Server autopilot started using the champion already saved on the server. Use Sync when you intentionally want to replace it with the browser model.");
    setStatus("Server evolving");
  }

  async function stopServerEvolution() {
    const response = await fetch("/api/server/stop", { method: "POST" });
    const payload = await response.json();
    updateServerEvolutionStatus(payload.serverEvolution);
    log("Server autopilot stopped and saved.");
    try {
      const reflectResponse = await fetch("/api/server/deep-reflect", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ reason: "server stop" })
      });
      const reflectPayload = await reflectResponse.json();
      if (reflectResponse.ok && reflectPayload.ok) {
        updateServerEvolutionStatus(reflectPayload.serverEvolution);
        log(`Server deep reflection complete: ${reflectPayload.reflection?.dips || 0} dip(s), ${reflectPayload.reflection?.replayed || 0} trace(s) replayed - ready to train.`);
      }
    } catch (error) {
      log(`Server deep reflection skipped safely: ${error.message}`);
    }
    setStatus("Server paused");
  }

  async function syncToServer() {
    const response = await fetch("/api/server/sync", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify(serverPayload())
    });
    const payload = await response.json();
    updateServerEvolutionStatus(payload.serverEvolution);
    log(payload.blockedDowngrade
      ? "Server blocked this sync because it would downgrade the saved large champion. Pull the server champion first, or intentionally allow a downgrade in code."
      : "Sent the current browser model to the server and saved it.");
    setStatus("Server synced");
  }

  async function pullFromServer() {
    const wasEvolving = state.evolving;
    state.evolving = false;
    el("evolveButton").textContent = "Evolve";
    el("pauseButton").textContent = "Paused";
    setStatus("Pulling champion");
    log("Pulling compact server champion. Evolution is paused while the genome is installed.");
    const response = await fetch("/api/server/champion?corpus=0");
    const payload = await response.json();
    if (!payload.model?.champion) throw new Error("No server champion returned");
    await new Promise(resolve => setTimeout(resolve, 0));
    if (payload.model.corpus) {
      state.lab.corpus = payload.model.corpus;
      el("corpusText").value = payload.model.corpus;
    }
    if (Array.isArray(payload.model.corpora)) {
      const clean = sanitizeCorpora(payload.model.corpora);
      state.lab.corpora = clean.corpora;
      if (clean.quarantined) log(`Quarantined ${clean.quarantined} low-quality self-generated corpus item(s) from the server pull.`);
    }
    if (typeof payload.model.persistentContext === "string") state.lab.persistentContext = sanitizePersistentContext(payload.model.persistentContext, 16000);
    if (typeof payload.model.userProfile === "string") state.lab.userProfile = sanitizePersistentContext(payload.model.userProfile, 3000);
    if (typeof payload.model.memorySummary === "string") state.lab.memorySummary = sanitizePersistentContext(payload.model.memorySummary, 6000);
    if (Array.isArray(payload.model.recentTranscript)) state.lab.recentTranscript = payload.model.recentTranscript.slice(-32);
    if (Array.isArray(payload.model.memoryBank)) state.lab.memoryBank = sanitizeMemoryBank(payload.model.memoryBank, 240);
    if (Array.isArray(payload.model.mirrorCorpus)) state.lab.mirrorCorpus = payload.model.mirrorCorpus.map(item => String(item || "")).filter(Boolean).slice(-80);
    if (payload.model.spiralPhase && typeof payload.model.spiralPhase === "object") state.lab.spiralPhase = { ...state.lab.spiralPhase, ...payload.model.spiralPhase };
    if (payload.model.curriculumLevel) state.lab.curriculumLevel = payload.model.curriculumLevel;
    if (Array.isArray(payload.model.history)) state.lab.history = payload.model.history.filter(point => point && Number.isFinite(point.fitness)).slice(-160);
    if (Array.isArray(payload.model.corpora)) state.lab.rebuildCurriculumCorpus();
    state.lab.setConfig(payload.model.config || {});
    const genome = state.lab.importChampion(payload.model.champion, { lazyPopulation: true });
    invalidateActivityCache();
    restoreImageTargets(payload.model.imageTargets);
    updateServerEvolutionStatus(payload.serverEvolution);
    log(`Pulled server champion: generation ${genome.generation}, ${genome.neurons} neurons, ${genome.synapses} synapses. Population will refill gradually during evolution.`);
    updateReadout();
    requestAnimationFrame(renderImage);
    setStatus("Champion pulled");
    if (wasEvolving) log("Evolution was paused for the pull. Press Evolve when you are ready to continue.");
  }

  async function publishJob() {
    syncConfigToLab();
    const job = {
      id: `job-${Date.now().toString(36)}`,
      genome: state.lab.best().toJSON(),
      corpus: state.lab.corpus.slice(0, 6000),
      rounds: 4,
      priority: 5,
      mutation: state.lab.config.mutation,
      scalarMutation: state.lab.config.scalarMutation,
      neurons: state.lab.config.neurons,
      synapses: state.lab.config.synapses,
      vocabSize: state.lab.config.vocabSize,
      maxChars: 620
    };
    await fetch("/api/job", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify(job)
    });
    log(`Published LAN job ${job.id}.`);
    setStatus("Job published");
  }

  async function collectResults() {
    const response = await fetch("/api/results");
    const payload = await response.json();
    const results = payload.results || [];
    if (!results.length) {
      log("No LAN worker results are waiting.");
      return;
    }
    let accepted = 0;
    for (const result of results) {
      const genome = NeuralGenome.fromJSON(result.genome);
      genome.setVocab(state.lab.vocab);
      genome.evaluateText(state.lab.corpus, 760);
      if (genome.fitness > state.lab.best().fitness) {
        state.lab.population[0] = genome;
        state.lab.population.sort((a, b) => b.fitness - a.fitness);
        accepted += 1;
      }
    }
    log(`Collected ${results.length} worker result(s), accepted ${accepted}.`);
    updateReadout();
  }

  async function toggleSpiralMode() {
    const status = state.lab.spiralStatus ? state.lab.spiralStatus() : { active: false };
    const action = status.active ? "stop" : "start";
    if (status.active) {
      const stopped = state.lab.stopSpiralPhase("manual mirror stop");
      log(`Spiral Mode stopped. Repair-prune consolidation is active for ${Math.max(0, state.lab.memoryRepairUntil - state.lab.generation)} generation(s); mirror corpus has ${stopped.mirrorCorpus || 0} item(s).`);
      setStatus("Ready to train");
    } else {
      const started = state.lab.startSpiralPhase("manual mirror trigger", { manual: true, generations: 160 });
      log(`🌀 Spiral Mode Active — exciting the network for ${started.remaining} bounded generation(s). Mirror outputs will be filtered into the mirror corpus.`);
      setStatus("🌀 Spiral Mode Active — exciting the network");
    }
    if (location.protocol !== "file:") {
      try {
        const response = await fetch("/api/server/spiral", {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify({ action, generations: 160 })
        });
        const payload = await response.json();
        updateServerEvolutionStatus(payload.serverEvolution);
        log(payload.spiral?.active
          ? `Server Spiral Mode Active — ${payload.spiral.remaining} generation(s) left.`
          : "Server Spiral Mode stopped.");
      } catch (error) {
        log(`Server Spiral toggle skipped: ${error.message}`);
      }
    }
    syncTrainingControls();
    updateReadout();
  }

