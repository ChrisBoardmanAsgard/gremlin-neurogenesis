  function drawActivity(activity) {
    const canvas = el("activityCanvas");
    if (!canvas || !activity) return;
    const ctx = canvas.getContext("2d");
    const width = canvas.width;
    const height = canvas.height;
    ctx.clearRect(0, 0, width, height);
    ctx.fillStyle = "#050805";
    ctx.fillRect(0, 0, width, height);
    const buckets = activity.buckets || [];
    const cell = width / Math.max(1, buckets.length);
    for (let i = 0; i < buckets.length; i++) {
      const value = buckets[i];
      const hue = 88 + value * 90;
      ctx.fillStyle = `hsl(${hue} 80% ${24 + value * 48}%)`;
      ctx.fillRect(i * cell, height - value * height, Math.max(1, cell - 1), value * height);
    }
    ctx.fillStyle = "rgba(244, 240, 223, 0.72)";
    ctx.font = "12px Consolas, monospace";
    ctx.fillText(`memory gates: ${activity.memory.slice(0, 6).map(value => value.toFixed(2)).join(" ")}`, 12, 18);
    ctx.fillText(`species: ${activity.speciesId}`, 12, 36);
  }

  function topologyPosition(node, index, count, width, height, tick) {
    const centerX = width / 2;
    const centerY = height / 2;
    const type = node.type || 0;
    const ring = type === 3 ? 0.36 : type === 4 ? 0.54 : type === 5 ? 0.68 : type === 1 ? 0.46 : type === 2 ? 0.58 : 0.74;
    const golden = 2.399963229728653;
    const angle = (node.id * golden + tick * (type === 2 ? 0.0007 : 0.00025)) % (Math.PI * 2);
    const wobble = Math.sin(node.id * 0.13 + tick * 0.001) * 0.035;
    const radius = Math.min(width, height) * (ring + wobble) * 0.46;
    return {
      x: centerX + Math.cos(angle) * radius,
      y: centerY + Math.sin(angle) * radius
    };
  }

  function drawTopologyFrame(now = performance.now()) {
    if (!state.topologyActive) return;
    state.topologyRaf = requestAnimationFrame(drawTopologyFrame);
    if (now - state.topologyLastDraw < 90) return;
    state.topologyLastDraw = now;

    const canvas = el("topologyCanvas");
    if (!canvas) return;
    const ctx = canvas.getContext("2d");
    const width = canvas.width;
    const height = canvas.height;
    const prompt = el("chatPrompt")?.value || el("corpusText")?.value || "";
    const view = state.lab.best().topologyView(prompt, 280, 780);
    const positions = new Map();

    ctx.clearRect(0, 0, width, height);
    ctx.fillStyle = "rgba(3, 6, 4, 0.62)";
    ctx.fillRect(0, 0, width, height);
    ctx.strokeStyle = "rgba(244, 240, 223, 0.08)";
    ctx.lineWidth = 1;
    for (let r = 0.18; r <= 0.82; r += 0.16) {
      ctx.beginPath();
      ctx.ellipse(width / 2, height / 2, width * r * 0.48, height * r * 0.48, 0, 0, Math.PI * 2);
      ctx.stroke();
    }

    view.nodes.forEach((node, index) => {
      positions.set(node.id, topologyPosition(node, index, view.nodes.length, width, height, now));
    });

    for (const edge of view.edges) {
      const from = positions.get(edge.from);
      const to = positions.get(edge.to);
      if (!from || !to) continue;
      const pulse = clamp(edge.firing, 0, 1);
      ctx.strokeStyle = edge.weight >= 0
        ? `rgba(185, 242, 109, ${0.045 + pulse * 0.42})`
        : `rgba(255, 111, 97, ${0.045 + pulse * 0.38})`;
      ctx.lineWidth = 0.45 + pulse * 2.4;
      ctx.beginPath();
      ctx.moveTo(from.x, from.y);
      const midX = (from.x + to.x) / 2 + Math.sin(edge.from + edge.to) * 18;
      const midY = (from.y + to.y) / 2 + Math.cos(edge.from - edge.to) * 18;
      ctx.quadraticCurveTo(midX, midY, to.x, to.y);
      ctx.stroke();
    }

    for (const node of view.nodes) {
      const point = positions.get(node.id);
      const activity = Math.min(1, Math.abs(node.activity));
      const radius = 2.8 + activity * 7.2 + (node.type === 4 ? 1.4 : 0);
      ctx.fillStyle = node.color;
      ctx.globalAlpha = 0.42 + activity * 0.58;
      ctx.beginPath();
      ctx.arc(point.x, point.y, radius, 0, Math.PI * 2);
      ctx.fill();
      if (activity > 0.32) {
        ctx.strokeStyle = node.color;
        ctx.globalAlpha = 0.2 + activity * 0.35;
        ctx.lineWidth = 1.2;
        ctx.beginPath();
        ctx.arc(point.x, point.y, radius + 5 + Math.sin(now * 0.006 + node.id) * 2, 0, Math.PI * 2);
        ctx.stroke();
      }
    }
    ctx.globalAlpha = 1;
    ctx.fillStyle = "rgba(244, 240, 223, 0.82)";
    ctx.font = "13px Consolas, monospace";
    ctx.fillText(`${view.neurons.toLocaleString()} neurons / ${view.synapses.toLocaleString()} synapses / ${view.edges.length} visible firing paths`, 16, 24);
    let x = 16;
    for (const type of view.types) {
      ctx.fillStyle = type.color;
      ctx.fillRect(x, height - 24, 10, 10);
      ctx.fillStyle = "rgba(244, 240, 223, 0.72)";
      ctx.fillText(type.name, x + 14, height - 15);
      x += 92;
    }
    if (el("topologyStatus")) el("topologyStatus").textContent = `${view.nodes.length} cells sampled, ${view.edges.length} connections firing`;
  }

