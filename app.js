  function drawFitness() {
    const canvas = el("fitnessCanvas");
    const ctx = canvas.getContext("2d");
    const width = canvas.width;
    const height = canvas.height;
    ctx.clearRect(0, 0, width, height);
    ctx.fillStyle = "#050805";
    ctx.fillRect(0, 0, width, height);
    ctx.strokeStyle = "rgba(244, 240, 223, 0.08)";
    ctx.lineWidth = 1;
    for (let i = 1; i < 5; i++) {
      const y = (height / 5) * i;
      ctx.beginPath();
      ctx.moveTo(0, y);
      ctx.lineTo(width, y);
      ctx.stroke();
    }

    const points = state.lab.history;
    if (points.length < 2) return;
    const max = Math.max(...points.map(point => point.fitness), 0.001);
    ctx.strokeStyle = "#b9f26d";
    ctx.lineWidth = 3;
    ctx.beginPath();
    points.forEach((point, index) => {
      const x = (index / (points.length - 1)) * width;
      const y = height - (point.fitness / max) * (height - 20) - 10;
      if (index === 0) ctx.moveTo(x, y);
      else ctx.lineTo(x, y);
    });
    ctx.stroke();
  }

